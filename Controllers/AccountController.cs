        //
        // POST: /Account/Login
        [HttpPost]
        [AllowAnonymous]
        [ValidateAntiForgeryToken]
        public async Task<ActionResult> Login(LoginViewModel model, string returnUrl)
        {
            Session["CUser"] = null;
            Hunar_DBEntities _db = new Hunar_DBEntities();
            if (!ModelState.IsValid)
            {
                return View(model);
            }

            // This doesn't count login failures towards account lockout
            // To enable password failures to trigger account lockout, change to shouldLockout: true
            var result = await SignInManager.PasswordSignInAsync(model.Email, model.Password, model.RememberMe, shouldLockout: false);
            switch (result)
            {
                case SignInStatus.Success:
                    var asptblID = _db.AspNetUsers.Where(x => x.UserName == model.Email.Trim())?.FirstOrDefault().Id;
                    var tbl = new tbl_UserLogin();
                    tbl.UserId = asptblID;
                    tbl.InDate = DateTime.Now;
                    tbl.IsActive = true;
                    _db.tbl_UserLogin.Add(tbl);
                    _db.SaveChanges();
                    var gteroles = await UserManager.GetRolesAsync(asptblID);
                    // If you just want the first role (assuming the user has only one role)
                    var roleName = gteroles?.FirstOrDefault();


                    if (CommonModel.RoleNameCont.Trainer == roleName)
                    {
                        return RedirectToAction("CreatedAttend", "Attendance");
                    }
                    else
                    {
                        return RedirectToAction("Dashboard", "Home");
                    }

                //return RedirectToLocal(returnUrl);
                case SignInStatus.LockedOut:
                    return View("Lockout");
                case SignInStatus.RequiresVerification:
                    return RedirectToAction("SendCode", new { ReturnUrl = returnUrl, RememberMe = model.RememberMe });
                case SignInStatus.Failure:
                default:
                    ModelState.AddModelError("", "Invalid login attempt.");
                    return View(model);
            }
        }

